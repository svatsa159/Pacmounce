    {
        for (int x=0; x<xSize; x++)
        {
            for (int z=0; z<zSize; z++)
            {
                if (locations[x,z] == 1)
                {
                    GameObject gameObject = Instantiate(arenaPrefab, new Vector3(x - xSize/2, 1, z - zSize/2), Quaternion.Euler(-90, 0, 0));
                    gameObject.transform.parent = transform;
                }
                else if (locations[x,z]==2)
                {
                    GameObject gameObject = Instantiate(FoodPrefab, new Vector3(x - xSize/2, 1, z - zSize/2), Quaternion.Euler(0, 0, 0));
